
n1 = float(input("Insira um numero flutante: "))

n2 = int(input("Insira um numero intero: "))

print(f"""
      {n1} multiplicado por {n2} é = {n1 * n2 :.2f}
      {n1} dividido por {n2} é = {n1 / n2 :.2f}
      {n1} mais {n2} é {n1 + n2} e {n1} menos {n2} é {n1 - n2 :.2f}
      {n1} elevado a {n2} é {n1 ** n2 :.2f}
      """)